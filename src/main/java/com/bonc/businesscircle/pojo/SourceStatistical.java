package com.bonc.businesscircle.pojo;

public class SourceStatistical {

    private String statMon;
    private String busCirId;
    private String busCirName;
    private String restWorkArea;
    private Integer busCirHNum;
    private String restWorkType;
    private Integer busCirNNum;

    public SourceStatistical() {
    }

    public String getStatMon() {
        return statMon;
    }

    public void setStatMon(String statMon) {
        this.statMon = statMon;
    }

    public String getBusCirId() {
        return busCirId;
    }

    public void setBusCirId(String busCirId) {
        this.busCirId = busCirId;
    }

    public String getBusCirName() {
        return busCirName;
    }

    public void setBusCirName(String busCirName) {
        this.busCirName = busCirName;
    }

    public String getRestWorkArea() {
        return restWorkArea;
    }

    public void setRestWorkArea(String restWorkArea) {
        this.restWorkArea = restWorkArea;
    }

    public Integer getBusCirHNum() {
        return busCirHNum;
    }

    public void setBusCirHNum(Integer busCirHNum) {
        this.busCirHNum = busCirHNum;
    }

    public String getRestWorkType() {
        return restWorkType;
    }

    public void setRestWorkType(String restWorkType) {
        this.restWorkType = restWorkType;
    }

    public Integer getBusCirNNum() {
        return busCirNNum;
    }

    public void setBusCirNNum(Integer busCirNNum) {
        this.busCirNNum = busCirNNum;
    }
}
